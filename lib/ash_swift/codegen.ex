defmodule AshSwift.Codegen do
  @moduledoc """
  Generates Swift source from a domain's reused `typescript_rpc` configuration.

  The generator is intentionally a plain module function: `build_files/1` is
  pure (domains in, `%{relative_path => source}` out) so tests can assert on the
  emitted source without touching the filesystem, and `generate/2` wraps it with
  deterministic, change-only writes.
  """

  require Logger

  alias AshTypescript.FieldFormatter
  alias AshTypescript.Resource.Info, as: ResourceInfo
  alias AshTypescript.Rpc.Info, as: RpcInfo

  # Built-in Swift types that are always safe to reference from related-resource
  # structs (no generated struct needed, never "2 hops away"). Includes scalar
  # types and AshJSON, which lives in the runtime package.
  @builtin_swift_types MapSet.new([
                         "String",
                         "Bool",
                         "Int",
                         "Double",
                         "Date",
                         "AshJSON"
                       ])

  # Complete set of Swift reserved keywords (Swift Language Reference §Lexical Structure).
  # Enum case names matching any of these must be backtick-escaped to produce valid Swift.
  #
  # Keywords used in declarations:
  #   associatedtype class deinit enum extension fileprivate func import init inout
  #   internal let open operator precedencegroup private protocol public rethrows
  #   static struct subscript typealias var
  # Keywords used in statements:
  #   break case catch continue default defer do else fallthrough for guard if in
  #   repeat return throw throws where while
  # Keywords used in expressions and types:
  #   Any as await catch false is nil rethrows self Self super throw throws true try
  # Keywords reserved in particular contexts (pattern / context keywords):
  #   associativity convenience didSet dynamic final get indirect infix lazy left
  #   mutating none nonisolated nonmutating optional override postfix precedence
  #   prefix Protocol required right set some Type unowned weak willSet
  # Swift Concurrency keywords:
  #   actor async distributed isolated
  @swift_reserved_keywords ~w(
    associatedtype class deinit enum extension fileprivate func import init inout
    internal let open operator precedencegroup private protocol public rethrows
    static struct subscript typealias var
    break case catch continue default defer do else fallthrough for guard if in
    repeat return throw throws where while
    Any as await false is nil self Self super true try
    associativity convenience didSet dynamic final get indirect infix lazy left
    mutating none nonisolated nonmutating optional override postfix precedence
    prefix Protocol required right set some Type unowned weak willSet
    actor async distributed isolated
  )

  @types_file "AshRpcTypes.swift"
  @functions_file "AshRpcFunctions.swift"

  @header """
  // Generated by AshSwift. Do not edit by hand.
  // Regenerate with `mix ash_swift.codegen`.
  """

  @doc """
  Builds the generated Swift source for the given domains.

  Returns a map of relative file path to file contents. Pure and deterministic:
  the same domains always produce byte-identical output.

  Raises `Mix.Error` if a generated enum type name collides with a resource
  struct type name (e.g. a resource named `TodoPriority` and a `priority` enum
  field on `Todo` both resolve to `TodoPriority`). The error names the colliding
  type and suggests the fix.
  """
  @spec build_files([module()]) :: %{String.t() => String.t()}
  def build_files(domains) when is_list(domains) do
    primary_resources = collect_resources(domains)
    all_resources = expand_with_related(primary_resources)

    %{
      @types_file => render_types(all_resources),
      @functions_file => render_functions(primary_resources)
    }
  end

  @doc """
  Builds the files for `domains` and writes them under `output_dir`, creating it
  if needed. Writes a file only when its contents changed, so regenerating with
  no schema change produces no diff (and no mtime churn).

  Returns `{:ok, [written_relative_paths]}` where the list is sorted and
  contains only files whose contents actually changed.
  """
  @spec generate([module()], String.t()) :: {:ok, [String.t()]}
  def generate(domains, output_dir) do
    files = build_files(domains)

    written =
      files
      |> Enum.sort_by(fn {path, _} -> path end)
      |> Enum.filter(fn {path, content} ->
        write_if_changed(Path.join(output_dir, path), content)
      end)
      |> Enum.map(fn {path, _} -> path end)

    {:ok, written}
  end

  @doc """
  Returns the sorted list of relative paths whose generated content differs from
  what is currently on disk under `output_dir` (a missing file counts as
  differing). An empty list means the committed output is up to date.

  This is the staleness check behind `mix ash_swift.codegen --check`: CI can fail
  when generated Swift hasn't been regenerated after a schema change (ADR-0005).
  """
  @spec stale_files([module()], String.t()) :: [String.t()]
  def stale_files(domains, output_dir) do
    domains
    |> build_files()
    |> Enum.filter(fn {path, content} ->
      full = Path.join(output_dir, path)
      current = if File.exists?(full), do: File.read!(full), else: :none
      current != content
    end)
    |> Enum.map(fn {path, _} -> path end)
    |> Enum.sort()
  end

  # Collects every RPC-exposed resource across the given domains, normalised into
  # a sorted list of maps the renderers consume. Sorting by Swift type name keeps
  # output deterministic regardless of domain/declaration order.
  defp collect_resources(domains) do
    domains
    |> Enum.flat_map(&RpcInfo.typescript_rpc/1)
    |> Enum.map(fn %{resource: resource, rpc_actions: rpc_actions} ->
      type_name = ResourceInfo.typescript_type_name!(resource)
      {fields, enums} = collect_fields(resource, type_name)
      formatter = AshTypescript.output_field_formatter() || :camel_case

      # Build actions and, in the same pass, collect the input structs to emit.
      {actions_unsorted, input_structs_unsorted} =
        Enum.reduce(rpc_actions, {[], []}, fn rpc_action, {actions_acc, structs_acc} ->
          ash_action = Ash.Resource.Info.action(resource, rpc_action.action)

          {is_get?, get_by_params, get_by_location} =
            if ash_action.type == :read do
              build_get_info(ash_action, rpc_action, resource, formatter)
            else
              {false, [], nil}
            end

          pagination_type =
            if ash_action.type == :read do
              action_pagination_type(ash_action)
            else
              :none
            end

          not_found_error? =
            case Map.get(rpc_action, :not_found_error?) do
              nil -> AshTypescript.Rpc.not_found_error?()
              value -> value
            end

          {input_struct_name, primary_key_params} =
            case ash_action.type do
              type when type in [:create, :update] ->
                struct_name = pascal_case(rpc_action.name) <> "Input"

                {struct_name,
                 build_get_by_params(Ash.Resource.Info.primary_key(resource), resource, formatter)}

              :destroy ->
                {nil,
                 build_get_by_params(Ash.Resource.Info.primary_key(resource), resource, formatter)}

              _ ->
                {nil, []}
            end

          action = %{
            rpc_name: rpc_action.name,
            action: rpc_action.action,
            action_type: ash_action.type,
            is_get?: is_get?,
            get_by_params: get_by_params,
            get_by_location: get_by_location,
            not_found_error?: not_found_error?,
            input_struct_name: input_struct_name,
            primary_key_params: primary_key_params,
            pagination_type: pagination_type
          }

          new_struct =
            if input_struct_name do
              inputs = collect_action_inputs(ash_action, resource, type_name, formatter)
              [%{struct_name: input_struct_name, fields: inputs}]
            else
              []
            end

          {[action | actions_acc], new_struct ++ structs_acc}
        end)

      %{
        resource_module: resource,
        type_name: type_name,
        fields: fields,
        enums: enums,
        actions: Enum.sort_by(actions_unsorted, & &1.rpc_name),
        input_structs: input_structs_unsorted |> Enum.sort_by(& &1.struct_name)
      }
    end)
    |> Enum.sort_by(& &1.type_name)
  end

  # Returns a tuple {fields, enums} for the resource.
  #
  # fields: sorted list of %{name, swift_type} for public scalar attributes and
  # public relationships. Field names go through the same formatter AshTypescript
  # uses for output. Enum attributes produce a generated Swift enum type name
  # instead of the default "String" fallback.
  #
  # enums: sorted list of %{enum_name, cases} for each attribute whose Ash type
  # is an enum (Ash.Type.Atom with one_of constraint, or Ash.Type.Enum subtype).
  defp collect_fields(resource, type_name) do
    formatter = AshTypescript.output_field_formatter() || :camel_case

    {attr_fields, enums} =
      resource
      |> Ash.Resource.Info.public_attributes()
      |> Enum.reduce({[], []}, fn attr, {fields_acc, enums_acc} ->
        formatted_name = FieldFormatter.format_field_for_client(attr.name, resource, formatter)

        case extract_enum_cases(attr) do
          {:ok, cases} ->
            en_name = enum_type_name(type_name, formatted_name)
            field = %{name: formatted_name, swift_type: en_name}
            enum = %{enum_name: en_name, cases: cases}
            {[field | fields_acc], [enum | enums_acc]}

          :not_enum ->
            field = %{name: formatted_name, swift_type: ash_type_to_swift(attr.type)}
            {[field | fields_acc], enums_acc}
        end
      end)

    rel_fields =
      resource
      |> Ash.Resource.Info.public_relationships()
      |> Enum.flat_map(fn rel ->
        case ResourceInfo.typescript_type_name(rel.destination) do
          {:ok, dest_type} ->
            swift_type =
              case rel.cardinality do
                :one -> dest_type
                :many -> "[#{dest_type}]"
              end

            name = FieldFormatter.format_field_for_client(rel.name, resource, formatter)
            [%{name: name, swift_type: swift_type}]

          :error ->
            []
        end
      end)

    fields = (attr_fields ++ rel_fields) |> Enum.sort_by(& &1.name)
    sorted_enums = enums |> Enum.sort_by(& &1.enum_name)
    {fields, sorted_enums}
  end

  # Augments the primary resource list with any related resources referenced by
  # their relationships that aren't already in the primary set. The related
  # resource's struct must be in the types file so the nested field decodes
  # correctly. Only one hop of relationship expansion is performed for M1.
  #
  # Related entries share the same map shape as primary entries (resource_module:
  # nil, actions: []) so render_types and render_functions always see uniform
  # maps regardless of which list they iterate.
  #
  # 2-hop guard: a related resource's own relationship fields may point to types
  # that are never emitted (2 hops away). Those fields are dropped so the
  # generated Swift compiles without undefined-type references. Enum fields are
  # always safe because their type is defined in the same file.
  defp expand_with_related(primary_resources) do
    primary_type_names = MapSet.new(primary_resources, & &1.type_name)

    related_raw =
      primary_resources
      |> Enum.flat_map(fn %{resource_module: resource} ->
        resource
        |> Ash.Resource.Info.public_relationships()
        |> Enum.flat_map(fn rel ->
          case ResourceInfo.typescript_type_name(rel.destination) do
            {:ok, type_name} ->
              if MapSet.member?(primary_type_names, type_name) do
                []
              else
                {fields, enums} = collect_fields(rel.destination, type_name)
                [%{type_name: type_name, fields: fields, enums: enums}]
              end

            :error ->
              []
          end
        end)
      end)
      |> Enum.uniq_by(& &1.type_name)

    all_type_names =
      MapSet.union(primary_type_names, MapSet.new(related_raw, & &1.type_name))

    # Enum types defined in this file are always safe to reference — include
    # them in the known-type set so the 2-hop guard doesn't drop enum fields.
    # Note: `related_raw` entries use the slim %{type_name, fields, enums} shape
    # (no :resource_module or :actions keys), so only :enums is accessed here.
    all_enum_type_names =
      (primary_resources ++ related_raw)
      |> Enum.flat_map(& &1.enums)
      |> MapSet.new(& &1.enum_name)

    collision = MapSet.intersection(all_type_names, all_enum_type_names)

    unless MapSet.size(collision) == 0 do
      [name | _] = collision |> MapSet.to_list() |> Enum.sort()

      Mix.raise(
        "AshSwift codegen: enum type name \"#{name}\" conflicts with resource struct name — " <>
          "rename the field that generates this enum or rename the resource."
      )
    end

    related =
      Enum.map(related_raw, fn %{type_name: type_name, fields: fields, enums: enums} ->
        safe_fields =
          Enum.filter(fields, fn %{swift_type: swift_type} ->
            swift_type_safe?(swift_type, all_type_names, all_enum_type_names)
          end)

        %{
          resource_module: nil,
          type_name: type_name,
          fields: safe_fields,
          enums: enums,
          actions: [],
          input_structs: []
        }
      end)

    (primary_resources ++ related) |> Enum.sort_by(& &1.type_name)
  end

  # Maps an Ash attribute type to its Swift counterpart. Enum attributes are
  # handled before this is called (via extract_enum_cases/1); this covers the
  # remaining scalar types and falls back to String for unknown types.
  #
  # Wire-format notes (confirmed from Jason.encode!/1 behaviour):
  #   Decimal        → JSON string "123.45" — Swift String preserves precision
  #   Date           → JSON string "2024-01-15" (date-only, no time component)
  #   UtcDatetime    → JSON string "2024-01-15T10:30:00Z" (ISO 8601 UTC)
  #   UtcDatetimeUsec → JSON string "2024-01-15T10:30:00.123456Z" (with μs)
  #   NaiveDatetime  → JSON string "2024-01-15T10:30:00" (no timezone)
  #   Map            → JSON object {…} — AshJSON handles any JSON value shape
  defp ash_type_to_swift(Ash.Type.UUID), do: "String"
  defp ash_type_to_swift(Ash.Type.String), do: "String"
  defp ash_type_to_swift(Ash.Type.Boolean), do: "Bool"
  defp ash_type_to_swift(Ash.Type.Integer), do: "Int"
  defp ash_type_to_swift(Ash.Type.Float), do: "Double"
  # Decimal arrives as a JSON string ("123.45") — String is the faithful Swift
  # type that preserves arbitrary precision without a custom decoder.
  defp ash_type_to_swift(Ash.Type.Decimal), do: "String"
  # Date-only ("2024-01-15"): Swift has no date-only type; String is correct.
  defp ash_type_to_swift(Ash.Type.Date), do: "String"
  # UTC datetimes arrive as ISO 8601 strings with "Z" suffix. AshRpcClient
  # configures its JSONDecoder with a custom date strategy to decode them into
  # Foundation Date values (see AshRpcClient.swift).
  defp ash_type_to_swift(Ash.Type.UtcDatetime), do: "Date"
  defp ash_type_to_swift(Ash.Type.UtcDatetimeUsec), do: "Date"
  # NaiveDatetime has no timezone info ("2024-01-15T10:30:00"); String avoids
  # the ambiguity of interpreting it in a caller-supplied timezone.
  defp ash_type_to_swift(Ash.Type.NaiveDatetime), do: "String"
  # Map arrives as an arbitrary JSON object. AshJSON (from AshSwiftRuntime)
  # handles any JSON value shape with a typed recursive enum.
  defp ash_type_to_swift(Ash.Type.Map), do: "[String: AshJSON]"
  # Atom without one_of constraints falls back to String. Atoms WITH one_of are
  # caught by extract_enum_cases/1 before reaching this function and become typed
  # Swift enums. A full atom-to-enum mapping is tracked in issue #24 (M2 plan).
  defp ash_type_to_swift(Ash.Type.Atom), do: "String"
  # Catch-all: emit a warning so unmapped types are visible rather than silently
  # stringified. Open an issue to add the explicit mapping if you hit this.
  defp ash_type_to_swift(type) do
    Logger.warning(
      "AshSwift: no Swift type mapping for Ash type #{inspect(type)}; " <>
        "falling back to String. Open an issue to add an explicit mapping."
    )

    "String"
  end

  # Returns {:ok, cases} when the attribute is an enum, :not_enum otherwise.
  # Handles Ash.Type.Atom with one_of constraint and Ash.Type.Enum subtypes.
  defp extract_enum_cases(attr) do
    cond do
      attr.type == Ash.Type.Atom ->
        case Keyword.get(attr.constraints || [], :one_of) do
          values when is_list(values) and values != [] -> {:ok, values}
          _ -> :not_enum
        end

      is_atom(attr.type) and attr.type != nil and
        Code.ensure_loaded?(attr.type) and
        function_exported?(attr.type, :values, 0) and
          Ash.Type.Enum in (attr.type.module_info(:attributes)
                            |> Keyword.get_values(:behaviour)
                            |> List.flatten()) ->
        {:ok, attr.type.values()}

      true ->
        :not_enum
    end
  end

  # Computes the Swift enum type name from the resource's Swift type name and the
  # formatted field name. E.g. type_name="Todo", field_name="priority" → "TodoPriority".
  # Only the first character of field_name is uppercased; the rest is preserved so
  # camelCase field names like "deliveryStatus" → "TodoDeliveryStatus".
  defp enum_type_name(type_name, field_name) do
    capitalized =
      case field_name do
        <<first::utf8, rest::binary>> -> String.upcase(<<first::utf8>>) <> rest
        "" -> ""
      end

    type_name <> capitalized
  end

  defp render_types(resources) do
    structs =
      Enum.map_join(resources, "\n\n", fn resource ->
        %{type_name: type_name, fields: fields, enums: enums, input_structs: input_structs} =
          resource

        enums_block =
          case enums do
            [] ->
              ""

            _ ->
              Enum.map_join(enums, "\n\n", &render_enum/1) <> "\n\n"
          end

        fields_block = render_fields(fields)

        model_struct =
          enums_block <>
            """
            /// Generated model for the `#{type_name}` resource.
            ///
            /// Every selectable field is Optional so that ad-hoc field selection is
            /// safe: unselected fields decode as `nil`.
            public struct #{type_name}: Codable, Sendable, Equatable {
            #{fields_block}    public init() {}
            }\
            """

        case input_structs do
          [] ->
            model_struct

          structs ->
            input_block = Enum.map_join(structs, "\n\n", &render_input_struct/1)
            model_struct <> "\n\n" <> input_block
        end
      end)

    IO.iodata_to_binary([
      @header,
      "\n",
      "import Foundation\n",
      "import AshSwiftRuntime\n",
      body_or_empty(structs)
    ])
  end

  defp render_fields([]), do: ""

  defp render_fields(fields) do
    fields
    |> Enum.map_join("\n", fn %{name: name, swift_type: type} ->
      "    public var #{escape_swift_keyword(name)}: #{type}?"
    end)
    |> Kernel.<>("\n")
  end

  defp render_enum(%{enum_name: name, cases: cases}) do
    cases_block =
      cases
      |> Enum.sort_by(&to_string/1)
      |> Enum.map_join("\n", fn c ->
        str = to_string(c)
        swift_case = lower_camel(str)

        safe_name = escape_swift_keyword(swift_case)

        if swift_case == str do
          "    case #{safe_name}"
        else
          "    case #{safe_name} = \"#{str}\""
        end
      end)

    """
    public enum #{name}: String, Codable, Sendable, Equatable {
    #{cases_block}
    }\
    """
  end

  defp render_functions(resources) do
    methods =
      resources
      |> Enum.flat_map(fn %{type_name: type_name, actions: actions} ->
        Enum.map(actions, &render_spec(method_spec(&1, type_name)))
      end)
      |> Enum.join("\n\n")

    # Indented method block, set off from the initializer by a blank line. Empty
    # when there are no actions, so the struct closes cleanly after `init`.
    methods_block = if methods == "", do: "", else: "\n\n" <> indent(methods)

    body = """
    /// Type-safe entry point for every RPC action exposed to this client.
    public struct AshRpc: Sendable {
        public let client: AshRpcClient

        public init(client: AshRpcClient) {
            self.client = client
        }#{methods_block}
    }
    """

    IO.iodata_to_binary([
      @header,
      "\n",
      "import Foundation\n",
      "import AshSwiftRuntime\n\n",
      body
    ])
  end

  # Renders a method spec into a Swift function. This is the single place any
  # generated function's shape is expressed — header, return clause, and the
  # `execute(SomeRequest(...))` body all live here. Per-shape variation comes in
  # as data via method_spec/2, so changing the Swift template (attributes,
  # docstring format, escaping) is a one-site edit rather than a six-clause audit.
  #
  # Spec keys:
  #   rpc_name      — the RPC action name (backtick-quoted in the docstring)
  #   func_name     — the Swift function name
  #   doc           — the parenthetical summary after "RPC action "
  #   params        — ordered [%{name, type, default}] (default nil = required)
  #   return_type   — Swift return type, or nil for a void function
  #   request_type  — the RpcRequest struct name passed to execute
  #   request_args  — ordered [{label, value}] for the request initializer
  defp render_spec(spec) do
    params_str =
      Enum.map_join(spec.params, ", ", fn
        %{name: n, type: t, default: nil} -> "#{n}: #{t}"
        %{name: n, type: t, default: d} -> "#{n}: #{t} = #{d}"
      end)

    args_str =
      Enum.map_join(spec.request_args, ", ", fn {label, value} -> "#{label}: #{value}" end)

    return_clause = if spec.return_type, do: " -> #{spec.return_type}", else: ""
    return_kw = if spec.return_type, do: "return ", else: ""

    """
    /// Calls the `#{spec.rpc_name}` RPC action (#{spec.doc}).
    public func #{escape_swift_keyword(spec.func_name)}(#{params_str}) async throws#{return_clause} {
        #{return_kw}try await client.execute(#{spec.request_type}(#{args_str}))
    }\
    """
  end

  # Classifies an action into a method spec (pure data — no Swift syntax). One
  # clause per action shape; render_spec/1 turns any of them into a function.

  # List (non-get read) actions accept a FieldSelection list. Return type and
  # the RpcRequest shape depend on the action's pagination configuration:
  #   - offset pagination → OffsetPage<TypeName> via OffsetPageRequest
  #   - keyset pagination → KeysetPage<TypeName> via KeysetPageRequest
  #   - no pagination     → [TypeName] via ListRequest
  defp method_spec(
         %{rpc_name: rpc_name, action_type: :read, is_get?: false, pagination_type: :offset},
         type_name
       ) do
    %{
      rpc_name: rpc_name,
      func_name: lower_camel(rpc_name),
      doc: "offset-paginated list of `#{type_name}` records",
      params: [%{name: "page", type: "OffsetPageParams?", default: "nil"}, fields_param()],
      return_type: "OffsetPage<#{type_name}>",
      request_type: "OffsetPageRequest",
      request_args: [{"action", swift_string(rpc_name)}, {"page", "page"}, {"fields", "fields"}]
    }
  end

  defp method_spec(
         %{rpc_name: rpc_name, action_type: :read, is_get?: false, pagination_type: :keyset},
         type_name
       ) do
    %{
      rpc_name: rpc_name,
      func_name: lower_camel(rpc_name),
      doc: "keyset-paginated list of `#{type_name}` records",
      params: [%{name: "page", type: "KeysetPageParams?", default: "nil"}, fields_param()],
      return_type: "KeysetPage<#{type_name}>",
      request_type: "KeysetPageRequest",
      request_args: [{"action", swift_string(rpc_name)}, {"page", "page"}, {"fields", "fields"}]
    }
  end

  defp method_spec(
         %{rpc_name: rpc_name, action_type: :read, is_get?: false},
         type_name
       ) do
    %{
      rpc_name: rpc_name,
      func_name: lower_camel(rpc_name),
      doc: "list `#{type_name}` records",
      params: [fields_param()],
      return_type: "[#{type_name}]",
      request_type: "ListRequest",
      request_args: [{"action", swift_string(rpc_name)}, {"fields", "fields"}]
    }
  end

  # Get (single-record read) actions: typed lookup params and a typed return.
  # not_found_error? true → return T (throws on missing).
  # not_found_error? false → return T? (nil on missing).
  defp method_spec(
         %{
           rpc_name: rpc_name,
           action_type: :read,
           is_get?: true,
           get_by_params: get_by_params,
           get_by_location: location,
           not_found_error?: not_found_error?
         },
         type_name
       ) do
    dict_entries =
      Enum.map_join(get_by_params, ", ", fn %{name: n} -> ~s("#{n}": #{n}) end)

    {request_type, return_type} =
      if not_found_error?,
        do: {"GetRequest", type_name},
        else: {"GetOptionalRequest", "#{type_name}?"}

    lookup_label = if location == :input, do: "input", else: "getBy"

    %{
      rpc_name: rpc_name,
      func_name: lower_camel(rpc_name),
      doc: "get `#{type_name}` record",
      params: Enum.map(get_by_params, &field_param/1) ++ [fields_param()],
      return_type: return_type,
      request_type: request_type,
      request_args: [
        {"action", swift_string(rpc_name)},
        {lookup_label, "[#{dict_entries}]"},
        {"fields", "fields"}
      ]
    }
  end

  # Create actions: typed input struct + returns the created record.
  defp method_spec(
         %{rpc_name: rpc_name, action_type: :create, input_struct_name: struct_name},
         type_name
       ) do
    %{
      rpc_name: rpc_name,
      func_name: lower_camel(rpc_name),
      doc: "create `#{type_name}` record",
      params: [%{name: "input", type: struct_name, default: nil}, fields_param()],
      return_type: type_name,
      request_type: "CreateRequest",
      request_args: [{"action", swift_string(rpc_name)}, {"input", "input"}, {"fields", "fields"}]
    }
  end

  # Update actions: primary-key params + typed input struct + returns the updated record.
  # M1: single primary-key field only; its value travels as the `identity` string
  # in the AshTypescript RPC wire protocol (ADR-0003).
  defp method_spec(
         %{
           rpc_name: rpc_name,
           action_type: :update,
           input_struct_name: struct_name,
           primary_key_params: pk_params
         },
         type_name
       ) do
    %{
      rpc_name: rpc_name,
      func_name: lower_camel(rpc_name),
      doc: "update `#{type_name}` record",
      params:
        Enum.map(pk_params, &field_param/1) ++
          [%{name: "input", type: struct_name, default: nil}, fields_param()],
      return_type: type_name,
      request_type: "UpdateRequest",
      request_args: [
        {"action", swift_string(rpc_name)},
        {"identity", pk_identity!(pk_params, type_name, "update")},
        {"input", "input"},
        {"fields", "fields"}
      ]
    }
  end

  # Destroy actions: primary-key param only, void return.
  # M1: single primary-key field only; its value travels as the `identity` string.
  defp method_spec(
         %{rpc_name: rpc_name, action_type: :destroy, primary_key_params: pk_params},
         type_name
       ) do
    %{
      rpc_name: rpc_name,
      func_name: lower_camel(rpc_name),
      doc: "destroy record",
      params: Enum.map(pk_params, &field_param/1),
      return_type: nil,
      request_type: "DestroyRequest",
      request_args: [
        {"action", swift_string(rpc_name)},
        {"identity", pk_identity!(pk_params, type_name, "destroy")}
      ]
    }
  end

  # Catch-all for any other action types not handled above.
  defp method_spec(%{rpc_name: rpc_name, action: action}, type_name) do
    %{
      rpc_name: rpc_name,
      func_name: lower_camel(rpc_name),
      doc: "`#{action}` on `#{type_name}`",
      params: [],
      return_type: nil,
      request_type: "RawRequest",
      request_args: [{"action", swift_string(rpc_name)}]
    }
  end

  # The `fields:` parameter every selectable action carries.
  defp fields_param, do: %{name: "fields", type: "[FieldSelection]", default: "[]"}

  # A lookup/primary-key field as a required Swift parameter.
  defp field_param(%{name: n, swift_type: t}), do: %{name: n, type: t, default: nil}

  # A string literal as it appears in emitted Swift source (quoted).
  defp swift_string(s), do: ~s("#{s}")

  # The primary-key field name used as the `identity` argument for update and
  # destroy. Warns on composite keys (only the first field is used until multi-PK
  # support lands) and raises when a resource has no primary key.
  defp pk_identity!(pk_params, type_name, verb) do
    case List.first(pk_params) do
      %{name: n} ->
        if length(pk_params) > 1 do
          Logger.warning(
            "AshSwift: resource #{type_name} has a composite primary key; " <>
              "#{verb} identity will only use the first field (#{n}) until multi-PK support lands."
          )
        end

        n

      nil ->
        raise "AshSwift: #{verb} codegen requires a primary key on resource #{type_name}"
    end
  end

  # ---- helpers ----

  # Returns true when a Swift type expression is safe to use in a related
  # resource struct — i.e., all referenced types are either built-in or
  # defined in the emitted output (resource types, enums).
  #
  # Handles: scalars ("String?"), arrays ("[Todo]?"), and dicts ("[String: AshJSON]").
  # The dict case strips the leading "[String: " and trailing "]" to get the
  # value type, then checks it independently.
  #
  # Known limitation: nested generics (e.g. "[String: [AshJSON]]") are not
  # handled — stripping one bracket layer yields "[AshJSON]" as the value type,
  # which won't match any entry in the builtin or type-name sets. No current code
  # path generates such types, so this is a future-maintainer note, not a bug.
  defp swift_type_safe?(swift_type, all_type_names, all_enum_type_names) do
    bare =
      swift_type
      |> String.trim_trailing("?")
      |> String.trim_leading("[")
      |> String.trim_trailing("]")

    if String.starts_with?(bare, "String: ") do
      value_type = String.replace_prefix(bare, "String: ", "")
      builtin_or_known?(value_type, all_type_names, all_enum_type_names)
    else
      builtin_or_known?(bare, all_type_names, all_enum_type_names)
    end
  end

  defp builtin_or_known?(type_name, all_type_names, all_enum_type_names) do
    MapSet.member?(@builtin_swift_types, type_name) or
      MapSet.member?(all_type_names, type_name) or
      MapSet.member?(all_enum_type_names, type_name)
  end

  defp body_or_empty(""), do: "\n"
  defp body_or_empty(structs), do: "\n" <> structs <> "\n"

  # Prefixes four spaces to each non-blank line of a (possibly multi-line)
  # string, leaving blank lines empty so no whitespace-only lines are emitted.
  defp indent(text) do
    text
    |> String.split("\n", trim: false)
    |> Enum.map_join("\n", fn
      "" -> ""
      line -> "    " <> line
    end)
  end

  # snake_case atom/string -> lowerCamelCase, the Swift idiom for functions and fields.
  defp lower_camel(name) do
    [first | rest] = name |> to_string() |> String.split("_", trim: true)
    Enum.join([String.downcase(first) | Enum.map(rest, &String.capitalize/1)])
  end

  # Determines whether a read action is a get action and, if so, collects the
  # lookup field params and where to place them in the request body.
  #
  # Returns {is_get?, get_by_params, location} where:
  #   - get_by_params: list of %{name, swift_type} for the lookup fields
  #   - location: :input (native get_by on the action) | :get_by (RPC-level get_by)
  defp build_get_info(ash_action, rpc_action, resource, formatter) do
    native_get_by = List.wrap(Map.get(ash_action, :get_by))
    rpc_get_by = Map.get(rpc_action, :get_by) || []
    rpc_get? = Map.get(rpc_action, :get?, false)

    cond do
      native_get_by != [] ->
        # get_by on the Ash action itself: fields travel in the `input` body key.
        {true, build_get_by_params(native_get_by, resource, formatter), :input}

      rpc_get_by != [] ->
        # get_by on the RPC action entity: fields travel in the `getBy` body key.
        {true, build_get_by_params(rpc_get_by, resource, formatter), :get_by}

      rpc_get? || Map.get(ash_action, :get?, false) ->
        # Pure get? (no explicit field list): look up by primary key via `input`.
        pk = Ash.Resource.Info.primary_key(resource)
        {true, build_get_by_params(pk, resource, formatter), :input}

      true ->
        {false, [], nil}
    end
  end

  # Maps a list of field atoms to the %{name, swift_type} shapes method_spec uses.
  defp build_get_by_params(fields, resource, formatter) do
    Enum.map(fields, fn field ->
      name = FieldFormatter.format_field_for_client(field, resource, formatter)
      %{name: name, swift_type: "String"}
    end)
  end

  # Collects the accepted input fields for a create or update action, mapping
  # each to its Swift type and whether it is required (create only).
  #
  # Only public attributes in the action's accept list are included; arguments
  # and non-public attributes are skipped. All update fields are optional because
  # update is always a partial operation.
  defp collect_action_inputs(ash_action, resource, type_name, formatter) do
    attrs_map =
      resource
      |> Ash.Resource.Info.public_attributes()
      |> Map.new(fn attr -> {attr.name, attr} end)

    ash_action.accept
    |> Enum.flat_map(fn field_name ->
      case Map.get(attrs_map, field_name) do
        nil ->
          Logger.warning(
            "AshSwift: field #{inspect(field_name)} in #{inspect(ash_action.name)}.accept " <>
              "is not a public attribute and will be omitted from the generated input struct. " <>
              "Action arguments are not yet supported."
          )

          []

        attr ->
          formatted_name = FieldFormatter.format_field_for_client(field_name, resource, formatter)

          swift_type =
            case extract_enum_cases(attr) do
              {:ok, _} -> enum_type_name(type_name, formatted_name)
              :not_enum -> ash_type_to_swift(attr.type)
            end

          required? = ash_action.type == :create && !attr.allow_nil? && is_nil(attr.default)
          [%{name: formatted_name, swift_type: swift_type, required?: required?}]
      end
    end)
    |> Enum.sort_by(& &1.name)
  end

  # Emits a typed input struct for a create or update action. Required fields are
  # non-optional stored properties and appear first in the init. Optional fields
  # use `encodeIfPresent` so that unset fields are omitted from the JSON body
  # rather than serialised as `null` (which would tell the backend to clear them).
  defp render_input_struct(%{struct_name: name, fields: []}) do
    """
    public struct #{name}: Encodable, Sendable {
        public init() {}
    }\
    """
  end

  defp render_input_struct(%{struct_name: name, fields: fields}) do
    all_sorted = fields
    required = Enum.filter(fields, & &1.required?)
    optional = Enum.filter(fields, &(!&1.required?))

    props_block =
      Enum.map_join(all_sorted, "\n", fn %{name: n, swift_type: t, required?: req?} ->
        sn = escape_swift_keyword(n)
        if req?, do: "    public var #{sn}: #{t}", else: "    public var #{sn}: #{t}?"
      end)

    init_params =
      (Enum.map(required, fn %{name: n, swift_type: t} ->
         "#{escape_swift_keyword(n)}: #{t}"
       end) ++
         Enum.map(optional, fn %{name: n, swift_type: t} ->
           "#{escape_swift_keyword(n)}: #{t}? = nil"
         end))
      |> Enum.join(", ")

    init_body =
      Enum.map_join(all_sorted, "\n", fn %{name: n} ->
        sn = escape_swift_keyword(n)
        "        self.#{sn} = #{sn}"
      end)

    coding_keys_cases =
      Enum.map_join(all_sorted, "\n", fn %{name: n} ->
        "        case #{escape_swift_keyword(n)}"
      end)

    encode_body =
      Enum.map_join(all_sorted, "\n", fn %{name: n, required?: req?} ->
        sn = escape_swift_keyword(n)

        if req?,
          do: "        try container.encode(#{sn}, forKey: .#{sn})",
          else: "        try container.encodeIfPresent(#{sn}, forKey: .#{sn})"
      end)

    """
    public struct #{name}: Encodable, Sendable {
    #{props_block}

        public init(#{init_params}) {
    #{init_body}
        }

        public func encode(to encoder: Encoder) throws {
            var container = encoder.container(keyedBy: CodingKeys.self)
    #{encode_body}
        }

        private enum CodingKeys: String, CodingKey {
    #{coding_keys_cases}
        }
    }\
    """
  end

  defp escape_swift_keyword(n) do
    if n in @swift_reserved_keywords, do: "`#{n}`", else: n
  end

  # snake_case to PascalCase (e.g. create_todo → CreateTodo).
  defp pascal_case(name) do
    name |> to_string() |> String.split("_", trim: true) |> Enum.map_join(&String.capitalize/1)
  end

  # Determines the pagination type for a read action by inspecting the action's
  # pagination configuration. Returns :offset, :keyset, or :none.
  #
  # Only returns a paginated type when `required?: true` — actions where pagination
  # is optional still return `[T]` (no page param sent → bare array response).
  # Actions where both offset? and keyset? are enabled default to :offset.
  defp action_pagination_type(ash_action) do
    case ash_action.pagination do
      %{required?: true, offset?: true} -> :offset
      %{required?: true, keyset?: true} -> :keyset
      _ -> :none
    end
  end

  defp write_if_changed(path, content) do
    current = if File.exists?(path), do: File.read!(path), else: :none

    if current == content do
      false
    else
      File.mkdir_p!(Path.dirname(path))
      File.write!(path, content)
      true
    end
  end
end
